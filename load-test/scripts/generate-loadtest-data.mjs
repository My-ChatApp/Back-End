#!/usr/bin/env node
/**
 * Generates seed.sql + users.csv for JMeter load tests.
 *
 * Usage (from Back-End):
 *   npm install --prefix load-test
 *   node load-test/scripts/generate-loadtest-data.mjs --count 500 --out-dir load-test/data
 */
import { createHash } from 'node:crypto';
import { mkdir, writeFile } from 'node:fs/promises';
import { dirname, join, resolve } from 'node:path';
import { fileURLToPath } from 'node:url';
import bcrypt from 'bcryptjs';

const __dirname = dirname(fileURLToPath(import.meta.url));
const DEFAULT_PASSWORD = 'LoadTest123!';
const EMAIL_DOMAIN = 'mychatapp.local';
const EMAIL_PREFIX = 'loadtest';

function parseArgs(argv) {
  let count = 500;
  let outDir = resolve(__dirname, '../data');
  let password = DEFAULT_PASSWORD;

  for (let i = 2; i < argv.length; i++) {
    const arg = argv[i];
    if (arg === '--count' && argv[i + 1]) {
      count = Number.parseInt(argv[++i], 10);
    } else if (arg === '--out-dir' && argv[i + 1]) {
      outDir = resolve(argv[++i]);
    } else if (arg === '--password' && argv[i + 1]) {
      password = argv[++i];
    } else if (arg === '--help' || arg === '-h') {
      console.log(`Usage: node generate-loadtest-data.mjs [--count 500] [--out-dir load-test/data] [--password '${DEFAULT_PASSWORD}']`);
      process.exit(0);
    }
  }

  if (!Number.isFinite(count) || count < 2 || count % 2 !== 0) {
    console.error('Error: --count must be an even integer >= 2');
    process.exit(1);
  }

  return { count, outDir, password };
}

/** Deterministic UUID from seed string (RFC 4122 variant). */
function uuidFromSeed(seed) {
  const hash = createHash('sha256').update(`mychatapp-loadtest:${seed}`).digest();
  const bytes = Buffer.from(hash.subarray(0, 16));
  bytes[6] = (bytes[6] & 0x0f) | 0x40;
  bytes[8] = (bytes[8] & 0x3f) | 0x80;
  const hex = bytes.toString('hex');
  return `${hex.slice(0, 8)}-${hex.slice(8, 12)}-${hex.slice(12, 16)}-${hex.slice(16, 20)}-${hex.slice(20, 32)}`;
}

function padNum(n, width) {
  return String(n).padStart(width, '0');
}

function sqlEscape(value) {
  return value.replace(/'/g, "''");
}

function buildUsers(count, passwordHash) {
  const width = String(count).length;
  const users = [];

  for (let i = 1; i <= count; i++) {
    const num = padNum(i, width);
    const email = `${EMAIL_PREFIX}${num}@${EMAIL_DOMAIN}`;
    const username = `${EMAIL_PREFIX}${num}`;
    const userId = uuidFromSeed(`user:${num}`);
    users.push({
      num: i,
      email,
      username,
      userId,
      displayName: `Load Test ${num}`,
      passwordHash,
    });
  }

  return users;
}

function buildConversations(users) {
  const conversations = [];
  const pairs = users.length / 2;

  for (let p = 0; p < pairs; p++) {
    const u1 = users[p * 2];
    const u2 = users[p * 2 + 1];
    const pairIndex = padNum(p + 1, String(pairs).length);
    const conversationId = uuidFromSeed(`conversation:${pairIndex}`);
    conversations.push({ conversationId, u1, u2 });
  }

  return conversations;
}

function buildSql(users, conversations) {
  const lines = [
    '-- Generated by load-test/scripts/generate-loadtest-data.mjs',
    '-- Idempotent: removes previous loadtest users and their conversations',
    'BEGIN;',
    '',
    "DELETE FROM app.conversation_members WHERE user_id IN (SELECT id FROM app.users WHERE email LIKE 'loadtest%@" +
      EMAIL_DOMAIN +
      "');",
    "DELETE FROM app.conversations WHERE created_by IN (SELECT id FROM app.users WHERE email LIKE 'loadtest%@" +
      EMAIL_DOMAIN +
      "');",
    "DELETE FROM app.friend_requests WHERE sender_id IN (SELECT id FROM app.users WHERE email LIKE 'loadtest%@" +
      EMAIL_DOMAIN +
      "') OR receiver_id IN (SELECT id FROM app.users WHERE email LIKE 'loadtest%@" +
      EMAIL_DOMAIN +
      "');",
    "DELETE FROM app.users WHERE email LIKE 'loadtest%@" + EMAIL_DOMAIN + "';",
    '',
  ];

  for (const u of users) {
    lines.push(
      `INSERT INTO app.users (id, email, username, password_hash, email_verified, display_name, is_active) VALUES (` +
        `'${u.userId}', '${sqlEscape(u.email)}', '${sqlEscape(u.username)}', '${sqlEscape(u.passwordHash)}', TRUE, '${sqlEscape(u.displayName)}', TRUE);`
    );
  }

  lines.push('');

  for (const c of conversations) {
    lines.push(
      `INSERT INTO app.conversations (id, type, created_by, deleted) VALUES ('${c.conversationId}', 'PRIVATE', '${c.u1.userId}', FALSE);`
    );
    lines.push(
      `INSERT INTO app.conversation_members (conversation_id, user_id, role, deleted) VALUES ('${c.conversationId}', '${c.u1.userId}', 'OWNER', FALSE);`
    );
    lines.push(
      `INSERT INTO app.conversation_members (conversation_id, user_id, role, deleted) VALUES ('${c.conversationId}', '${c.u2.userId}', 'MEMBER', FALSE);`
    );
  }

  lines.push('', 'COMMIT;', '');
  return lines.join('\n');
}

function buildCsv(users, conversations, password) {
  const convByUserId = new Map();
  for (const c of conversations) {
    convByUserId.set(c.u1.userId, c.conversationId);
    convByUserId.set(c.u2.userId, c.conversationId);
  }

  const header = 'email,password,userId,conversationId';
  const rows = users.map((u) => {
    const conversationId = convByUserId.get(u.userId);
    if (!conversationId) {
      throw new Error(`No conversation for user ${u.email}`);
    }
    return `${u.email},${password},${u.userId},${conversationId}`;
  });

  return [header, ...rows].join('\n') + '\n';
}

async function main() {
  const { count, outDir, password } = parseArgs(process.argv);

  console.log(`Generating load-test data for ${count} users...`);
  const passwordHash = bcrypt.hashSync(password, 10);

  const users = buildUsers(count, passwordHash);
  const conversations = buildConversations(users);
  const sql = buildSql(users, conversations);
  const csv = buildCsv(users, conversations, password);

  await mkdir(outDir, { recursive: true });

  const sqlPath = join(outDir, 'seed.sql');
  const csvPath = join(outDir, 'users.csv');

  await writeFile(sqlPath, sql, 'utf8');
  await writeFile(csvPath, csv, 'utf8');

  console.log(`Wrote ${sqlPath}`);
  console.log(`Wrote ${csvPath}`);
  console.log(`Users: ${users.length}, DM conversations: ${conversations.length}`);
  console.log(`Password (all users): ${password}`);
  console.log('');
  console.log('Apply seed to Postgres:');
  console.log(`  Get-Content "${sqlPath}" | docker exec -i mychatapp-postgres psql -U mychatapp -d mychatapp`);
}

main().catch((err) => {
  console.error(err);
  process.exit(1);
});
